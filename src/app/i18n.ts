export function translate(language: Language, key: string, values: TranslationValues = {}): string {
  const template = (language === "zh" ? zh[key] : en[key]) ?? key;
  return template.replace(/\{(\w+)\}/g, (_, name: string) => String(values[name] ?? `{${name}}`));
}

